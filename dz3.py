def city_country(city,country):
    city_country = city.title() + "," + country.title()
    return city_country

city_country1 = city_country('tehas','usa')
print(city_country1)

city_country2 = city_country('moscow','russia')
print(city_country2)

city_country3 = city_country('kiev','ukraine')
print(city_country3)
